    public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ClientesDAO dao = new ClientesDAO();
        //Setando as linhas com os itens no banco de dados.
        for(Clientes c: dao.read()){
            modelo.addRow(new Object[]{
            c.getCodCli(),
            c.getNome(),
            c.getEndereco(),
            c.getEstado()
            });
        }
    }
    //Metodo que cria uma linha sobre um cliente especifico.
    public void readJTableClient(String nome){
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ClientesDAO dao = new ClientesDAO();
        
        for(Clientes c:dao.readSelectedClient(nome)){
            modelo.addRow(new Object[]{
                c.getCodCli(),
                c.getNome(),
                c.getEndereco(),
                c.getEstado()
            });
        }
    }
    
