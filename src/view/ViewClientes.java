    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
       
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        jTable1.setRowSorter(new TableRowSorter(modelo));
        
        //Limpando os campos...
        txtNome.setText("");
        cbEstado.setSelectedItem("Selecione");
        txtEndereco.setText("");
        //Excluindo todas as linhas...
        for(int i = modelo.getRowCount()-1;i>=0;i--){
            modelo.removeRow(i);
        }
        
    }//GEN-LAST:event_btnLimparActionPerformed

    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        
        //Verificando se os campos estão vazios...
        if(txtNome.getText().isEmpty() || cbEstado.getSelectedItem() == "Selecione"
                || txtEndereco.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!");
        } else {
            Clientes c = new Clientes();
            ClientesDAO dao = new ClientesDAO();
            //Recebendo todos os campos.
            c.setNome(txtNome.getText());
            c.setEndereco(txtEndereco.getText());
            c.setEstado((String) cbEstado.getSelectedItem());
            //Criando um cliente no banco de dados.
            dao.create(c);
            //Limpando campos.
            txtNome.setText("");
            cbEstado.setSelectedItem("Selecione");
            txtEndereco.setText("");
            //Atualizando a tabela...
            readJTable();
            }
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
        //Verificando se alguma linha está selecionada.
        if(jTable1.getSelectedRow() != -1){
            Clientes c = new Clientes();
            ClientesDAO dao = new ClientesDAO();
            
            //Pegando os valores da tabela.
            c.setCodCli((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            c.setNome(txtNome.getText());
            c.setEndereco(txtEndereco.getText());
            c.setEstado((String)cbEstado.getSelectedItem());
            //Verificando se os campos estão vazios.
            if(txtNome.getText().isEmpty() || cbEstado.getSelectedItem() == "Selecione"
                || txtEndereco.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!");
            } else {
                //Atualizando o objeto cliente.
                dao.update(c);
                //Limpando os campos...
                txtNome.setText("");
                txtEndereco.setText("");
                cbEstado.setSelectedItem("Selecione");
                //Atualizando a tabela.
                readJTable();
            }
        } else {
            
            JOptionPane.showMessageDialog(null, "Nenhum cliente foi selecionado!");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        //Colocando os valores da linha nos textfields.
        if(jTable1.getSelectedRow() != -1){
            txtNome.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            txtEndereco.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
            cbEstado.setSelectedItem(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        
        if(jTable1.getSelectedRow() != -1){
            Clientes c = new Clientes();
            ClientesDAO dao = new ClientesDAO();
            
            c.setCodCli((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            
            if(txtNome.getText().isEmpty() || cbEstado.getSelectedItem() == "Selecione"
                || txtEndereco.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!");
            } else {
            dao.delete(c);
            
            txtNome.setText("");
            txtEndereco.setText("");
            cbEstado.setSelectedItem("Selecione");
            
            readJTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum cliente foi selecionado!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

