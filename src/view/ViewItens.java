    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ItensDAO dao = new ItensDAO();
        
        for(Itens i: dao.read()){
            modelo.addRow(new Object[]{
            i.getNotaFiscal().getNumero(),
            i.getNotaFiscal().getSerie(),
            i.getItem(),
            i.getPneu().getCodPneu(),
            i.getQtde(),
            i.getPreco()
            });
        }
    }
    
    public void readJTableItens(int numero, int item){
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
        modelo.setNumRows(0);
        ItensDAO dao = new ItensDAO();
        
        for(Itens i:dao.readSelectedItem(numero, item)){
            modelo.addRow(new Object[]{
            i.getNotaFiscal().getNumero(),
            i.getNotaFiscal().getSerie(),
            i.getItem(),
            i.getPneu().getCodPneu(),
            i.getQtde(),
            i.getPreco()
            });
        }
    }

