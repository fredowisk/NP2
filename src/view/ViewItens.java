    private void btnLimpar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpar2ActionPerformed
        
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
        jTable1.setRowSorter(new TableRowSorter(modelo));
        
        txtNumero.setText("");
        txtItem.setText("");
        txtPneu.setText("");
        txtPreco.setText("");
        txtQtde.setText("");
        txtSerie.setText("");
        
        for(int i = modelo.getRowCount()-1; i>=0; i--){
            modelo.removeRow(i);
        }
        
    }//GEN-LAST:event_btnLimpar2ActionPerformed

    private void btnIncluir2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluir2ActionPerformed
        
        
        if(txtItem.getText().isEmpty() || txtPneu.getText().isEmpty() || txtPreco.getText().isEmpty() 
                || txtQtde.getText().isEmpty() || txtSerie.getText().isEmpty()){
            
        JOptionPane.showMessageDialog(null, "Preencha os campos corretamente!");
    } else {
            Itens i = new Itens();
            ItensDAO dao = new ItensDAO();
            NotaFiscal n = new NotaFiscal();
            Pneus p = new Pneus();
            
            n.setNumero(Integer.parseInt(txtNumero.getText()));
            n.setSerie(txtSerie.getText());
            i.setItem(Integer.parseInt(txtItem.getText()));
            p.setCodPneu(Integer.parseInt(txtPneu.getText()));
            i.setQtde(Integer.parseInt(txtQtde.getText()));
            i.setPreco(Double.parseDouble(txtPreco.getText()));
            i.setNotaFiscal(n);
            i.setPneu(p);
            dao.create(i);
            
            txtNumero.setText("");
            txtItem.setText("");
            txtPneu.setText("");
            txtPreco.setText("");
            txtQtde.setText("");
            txtSerie.setText("");
            
            readJTable();
        }
    }//GEN-LAST:event_btnIncluir2ActionPerformed

    private void btnAlterar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterar2ActionPerformed
        
        
        if(jTable1.getSelectedRow() != -1){
            Itens i = new Itens();
            ItensDAO dao = new ItensDAO();
            NotaFiscal n = new NotaFiscal();
            Pneus p = new Pneus();
            
            n.setNumero(Integer.parseInt(txtNumero.getText()));
            i.setItem(Integer.parseInt(txtItem.getText()));
            p.setCodPneu(Integer.parseInt(txtPneu.getText()));
            i.setPreco(Double.parseDouble(txtPreco.getText()));
            i.setQtde(Integer.parseInt(txtQtde.getText()));
            n.setSerie(txtSerie.getText());
            i.setNotaFiscal(n);
            i.setPneu(p);
            
            if(txtItem.getText().isEmpty() || txtPneu.getText().isEmpty() || txtPreco.getText().isEmpty() 
                || txtQtde.getText().isEmpty() || txtSerie.getText().isEmpty()){
            
                JOptionPane.showMessageDialog(null, "Preencha os campos corretamente!");
            }else {
                dao.update(i);
                
                txtNumero.setText("");
                txtItem.setText("");
                txtPneu.setText("");
                txtPreco.setText("");
                txtQtde.setText("");
                txtSerie.setText("");
            
                readJTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum item foi selecionado!");
        }
    }//GEN-LAST:event_btnAlterar2ActionPerformed

