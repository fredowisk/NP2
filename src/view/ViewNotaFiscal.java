    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
        modelo.setNumRows(0);
        NotaFiscalDAO dao = new NotaFiscalDAO();
        
        for(NotaFiscal n:dao.read())
            modelo.addRow(new Object[]{
                n.getNumero(),
                n.getSerie(),
                n.getCliente().getCodCli(),
                n.getData(),
                n.getCancelada()
            });
    }
    
    public void readJTableNota(int numero){
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
        modelo.setNumRows(0);
        NotaFiscalDAO dao = new NotaFiscalDAO();
        
        for(NotaFiscal n: dao.readSelectedNota(numero)){
            modelo.addRow(new Object[]{
            n.getNumero(),
            n.getSerie(),
            n.getCliente().getCodCli(),
            n.getData(),
            n.getCancelada()
            });
        }
    }

