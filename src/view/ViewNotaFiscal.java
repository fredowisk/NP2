    private void btnIncluir4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluir4ActionPerformed
        
        
        if(txtSerie.getText().isEmpty() ||
                txtCodCli.getText().isEmpty() || txtData.getText().isEmpty() || txtCancelada.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!");
        } else {
            NotaFiscal n = new NotaFiscal();
            NotaFiscalDAO dao = new NotaFiscalDAO();
            Clientes c = new Clientes();
            
            n.setSerie(txtSerie.getText());
            c.setCodCli(Integer.parseInt(txtCodCli.getText()));
            
            java.sql.Date data = null;
            try {
                
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            data = new java.sql.Date(format.parse(txtData.getText()).getTime());
            } catch (ParseException e) {
                
                JOptionPane.showMessageDialog(null, "Erro na conversão de data!");
            }
            
            n.setData(data);
            if(txtCancelada.getText().equals("S") || txtCancelada.getText().equals("N")){
                n.setCancelada(txtCancelada.getText()); 
            
                n.setCliente(c);
                dao.create(n);
            
            
                txtSerie.setText("");
                txtCodCli.setText("");
                txtData.setText("");
                txtCancelada.setText("");
            
                readJTable();
            } else {
                JOptionPane.showMessageDialog(null, "O campo CANCELADA só pode ser informado com 'S' ou 'N' !");
            }
        }
    }//GEN-LAST:event_btnIncluir4ActionPerformed

